#R supports various data types, including 
    # 1. numeric, 
    # 2. character, 
    # 3. logical (Boolean), 
    # 4. integer, 
    # 5. complex

#numeric
var1 = 27.02
print(var1)
print(class(var1))

#character
var2 = "Sky"
print(var2)
print(class(var2))

#logical/boolean
is_true <- TRUE
print(is_true)
print(class(is_true))

#complex
var4 = 12-9i
print(var4)
print(class(var4))